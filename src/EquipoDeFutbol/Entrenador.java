package EquipoDeFutbol;

public class Entrenador {
	
	//Variables
	private String nombreEntrnador;
	
	private String Nacionalidad;
	
	//Getters y Setters
	public String getNombreEntrnador() {
		return nombreEntrnador;
	}
	
	public void setNacionalidad(String nacionalidad) {
		Nacionalidad = nacionalidad;
	}

	public void setNombreEntrnador(String nombreEntrnador) {
		this.nombreEntrnador = nombreEntrnador;
	}

	public String getNacionalidad() {
		return Nacionalidad;
	}

	
	
}

