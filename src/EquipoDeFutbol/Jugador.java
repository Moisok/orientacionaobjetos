package EquipoDeFutbol;

public class Jugador {
	
	//Variables
	
	private String nomnbreJugador;
	
	private String Nacionalidad;
	
	private String posicionJuego;
	
	//Getters y setters

	public String getNomnbreJugador() {
		return nomnbreJugador;
	}

	public void setNomnbreJugador(String nomnbreJugador) {
		this.nomnbreJugador = nomnbreJugador;
	}

	public String getNacionalidad() {
		return Nacionalidad;
	}

	public void setNacionalidad(String nacionalidad) {
		Nacionalidad = nacionalidad;
	}

	public String getPosicionJuego() {
		return posicionJuego;
	}

	public void setPosicionJuego(String posicionJuego) {
		this.posicionJuego = posicionJuego;
	}
	

	
	

}
